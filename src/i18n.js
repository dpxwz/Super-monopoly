// i18n module — lightweight zh/en localization for Super Monopoly.
//
// Design: a flat key→string dictionary per locale, plus a small set of
// composable label helpers used by both game.js (error / log messages) and
// ui.js (rendered DOM text). Locale is detected from the browser on load and
// can be changed at runtime via setLocale().

let currentLocale = 'zh';

export function detectLocale() {
  if (typeof navigator !== 'undefined' && navigator.language) {
    currentLocale = navigator.language.toLowerCase().startsWith('zh') ? 'zh' : 'en';
  }
  return currentLocale;
}

export function setLocale(locale) {
  currentLocale = locale === 'en' ? 'en' : 'zh';
}

export function getLocale() {
  return currentLocale;
}

// Core translation lookup. Extra positional args replace {0}, {1}, … in the
// translated string.
export function t(key, ...args) {
  const table = currentLocale === 'en' ? EN : ZH;
  let str = table[key] ?? ZH[key] ?? key;
  args.forEach((arg, i) => {
    str = str.replace(`{${i}}`, String(arg));
  });
  return str;
}

// ---------- Localized name helpers ----------
// City and color data in game.js keeps both nameZh and name (English).
// These helpers pick the right one for the active locale.

export function cityName(space) {
  if (!space) return '';
  if (space.type === 'start') return t('space.start');
  return currentLocale === 'en' ? (space.nameEn || space.name || '') : (space.name || space.nameZh || '');
}

export function countryName(space) {
  if (!space) return '';
  return currentLocale === 'en' ? (space.countryNameEn || '') : (space.countryName || '');
}

export function colorName(space) {
  if (!space) return '';
  return currentLocale === 'en' ? (space.colorNameEn || '') : (space.colorName || '');
}

// ---------- Composable labels used by game.js and ui.js ----------

export function startDescription(bonus) {
  return currentLocale === 'en'
    ? `Pass or land here to receive $${bonus}`
    : `经过或停在这里获得 $${bonus}`;
}

export function playerDefaultName(index) {
  return currentLocale === 'en' ? `Player ${index}` : `玩家 ${index}`;
}

// Shareholders are rendered as "Name1 30%, Name2 20%, …". The separator and
// the "Bank" label are locale-dependent.
export function formatShareholders(holders, playerNameFn) {
  if (!holders || holders.length === 0) return t('label.none');
  return holders.map((h) => {
    const name = h.playerId === 'bank' ? t('entity.bank') : playerNameFn(h.playerId);
    return `${name} ${h.percent}%`;
  }).join(', ');
}

